package Dia2402IntroducaoAoPOO;

public class Carro {
    String cor;
    int quantidadeDeGasolina;
    int quantidadeDePortas;
    String motor;
    String marca;

    public Carro(String cor, int quantidadeDeGasolina, int quantidadeDePortas, String motor, String marca) {
        this.cor = cor;
        this.quantidadeDeGasolina = quantidadeDeGasolina;
        this.quantidadeDePortas = quantidadeDePortas;
        this.motor = motor;
        this.marca = marca;
    }
}
